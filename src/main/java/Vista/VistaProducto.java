    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        try{
            ControlProducto objCtrProducto = new ControlProducto(); 
            Categoria objCategoria = new Categoria(); 
            
            String nombre = this.txtNombre.getText(); 
            String cantidad = this.txtCantidad.getText(); 
            String precio = this.txtPrecio.getText(); 
            
            if(this.cboxCategoria.getSelectedItem()=="Seleccione"){
                 JOptionPane.showMessageDialog(null, "ERROR: Selecciones una categoria valida"); 
            }
            else if(this.cboxCategoria.getSelectedItem().equals("Lacteos")){
                objCategoria.setIdCategoria(1);
                objCategoria.setNombreCategoria("Lacteos");
            }
            else if(this.cboxCategoria.getSelectedItem().equals("Abarrotes")){
                objCategoria.setIdCategoria(2);
                objCategoria.setNombreCategoria("Abarrotes");
            }
            else if(this.cboxCategoria.getSelectedItem().equals("Carnicos")){
                objCategoria.setIdCategoria(3);
                objCategoria.setNombreCategoria("Carnicos");
            }
            else if(this.cboxCategoria.getSelectedItem().equals("Aseo")){
                objCategoria.setIdCategoria(5);
                objCategoria.setNombreCategoria("Aseo");
            }
            
            objCtrProducto.crear(nombre, cantidad, precio, objCategoria);
            JOptionPane.showMessageDialog(null, "Insertado correctamente!!");
            
            this.txtNombre.setText("");
            this.txtCantidad.setText("");
            this.txtPrecio.setText("");
            this.cboxCategoria.setSelectedItem("Seleccione");
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "ERROR: "+error);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

