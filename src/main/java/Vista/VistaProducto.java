    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
         try {
            ControlProducto crtProducto = new ControlProducto(); 
            DefaultTableModel tabla = new DefaultTableModel(); 
            ArrayList<Producto> consultaProducto = new ArrayList<>(); 
            
            String nombre = this.txtNomConsulta.getText(); 
            consultaProducto = crtProducto.consultar(nombre); 
            
            tabla.addColumn("ID");
            tabla.addColumn("NOMBRE");
            tabla.addColumn("CANTIDAD");
            tabla.addColumn("PRECIO");
            
            this.jTable1.setModel(tabla);
            
            String[] dato = new String[4]; 
            
            for(int i=0; i<consultaProducto.size();i++){
                dato[0] = String.valueOf(consultaProducto.get(i).getId()); 
                dato[1] = consultaProducto.get(i).getNombre(); 
                dato[2] = String.valueOf(consultaProducto.get(i).getCantidad()); 
                dato[3] = String.valueOf(consultaProducto.get(i).getPrecio()); 
                
                tabla.addRow(dato);
            }
            
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "ERROR: "+error);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

