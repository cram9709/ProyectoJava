    public void listar(){
        try {
            ControlProducto crtProducto = new ControlProducto(); 
            DefaultTableModel tabla = new DefaultTableModel(); 
            ArrayList<Producto> listaProducto = new ArrayList<>(); 
            
            listaProducto = crtProducto.listar(); 
            
            tabla.addColumn("ID");
            tabla.addColumn("NOMBRE");
            tabla.addColumn("CANTIDAD");
            tabla.addColumn("PRECIO");
            
            this.jTable1.setModel(tabla);
            
            String[] dato = new String[4]; 
            
            for(int i=0; i<listaProducto.size();i++){
                dato[0] = String.valueOf(listaProducto.get(i).getId()); 
                dato[1] = listaProducto.get(i).getNombre(); 
                dato[2] = String.valueOf(listaProducto.get(i).getCantidad()); 
                dato[3] = String.valueOf(listaProducto.get(i).getPrecio()); 
                
                tabla.addRow(dato);
            }
            
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "ERROR: "+error);
        }
    } 
    
    //METODO LLENAR COMBOBOX: 
    public void llenarCombo(){
        try{
            ControlCategoria objCtrCategoria = new ControlCategoria(); 
            ArrayList<Categoria> listaCbox = new ArrayList<>();
            
            listaCbox = objCtrCategoria.cargarCbox(); 
            
            for (int i=0; i<listaCbox.size(); i++){
                this.cboxCategoria.addItem(listaCbox.get(i).getNombreCategoria());
            }
            
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "ERROR: "+error);
        }
    }

